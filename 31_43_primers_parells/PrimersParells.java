/*Aquest programa  mostra de més petit a més gran
/tots els nombres parells que hi ha entre 1 i 10
*/
public class PrimersParells {	
	public static void main (String[] args) {
	
	int numero = 2;        

while (numero <= 10) {  
	if (numero % 2 == 0) {
	
    System.out.println(numero);
    }

    numero = numero + 1;    
    }

}
}

		 
